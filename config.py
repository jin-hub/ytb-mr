TIMEZONE = "Asia/Seoul"   # 韩国时间 KST (UTC+9)，图表/表格/时间都用它

# ===== 采集 =====
FETCH_INTERVAL_MIN = 5    # 名义采集间隔（分钟）。GitHub Actions 实际可能略有延迟，已用速率算法消除影响。

# ===== 数据缺失判定 =====
GAP_THRESHOLD_MIN = 12    # 相邻两点间隔超过这么多分钟，视为数据缺失，缺失段不参与速率/异常判断

# ===== 定时推送趋势图的时间点（从每场“本轮开始”算起，单位：小时）=====
# 到达每个点时，推一次“该跨度”的趋势图（点赞图+播放图+数据表图）
PUSH_MILESTONES_H = [0.5, 1, 3, 6, 24, 48, 72]
# 48h 只画第 2 天数据（24~48h），72h 只画第 3 天数据（48~72h）
WINDOWED_MILESTONES = {48: (24, 48), 72: (48, 72)}

# ===== 异常检测（报警）参数 — 台阶检测法 =====
# 核心思路：抓“孤立陡峰”——某成员速度突然冲到平时的若干倍，之后又落回正常。
# 全部相对各成员自己的“平时速度”，与纵轴缩放无关。

# 多长时间算一段速度：用相邻两点（约5分钟）的累计增量算“每分钟速度”
# “平时速度”取突变点之前这么多个速度点的中位数作为基线
BASELINE_WINDOW = 8          # 平时速度参考窗口（突变前最近 N 个速度点，约 40 分钟）
MIN_POINTS_TO_JUDGE = 6      # 冷启动：速度点不足这么多就先不判断（避免视频刚发布时乱报）

# 条件①②：突变段速度 ≥ 平时速度的多少倍，才算“明显变陡”
SPIKE_MULT = 3.5             # 你定的 3.5 倍

# 基线速度绝对下限：平时每分钟涨幅低于这个值的成员，视为“低基数平缓型”，
# 其微小波动不参与倍数判断（避免 准书/상현 这种平直线因小数除法虚高而误报）。
MIN_BASELINE_RATE = 3.0      # 每分钟至少涨3（约5分钟涨15）才参与台阶判断

# 突变绝对增量下限：突变段的“多涨出来的量”也要够大，否则不算（双保险）
MIN_SPIKE_ABS = 50.0         # 突变段比平时多涨的累计量，至少这么多才算

# 回落判定：突变后，速度要落回平时速度的多少倍以内，才算“台阶”（冲上去又下来）
FALLBACK_MULT = 1.5          # 落回平时的 1.5 倍以内算回落

# 找台阶时，最多往后看几个速度点等它回落（约 5 分钟一个点）
FALLBACK_LOOKAHEAD = 3       # 突变后最多看 3 个点（约15分钟）内有没有回落

# 条件③：是否“全员一起涨”。突变时刻，如果出现同样陡峰的成员比例超过这个值，
# 判定为自然现象（大家一起涨），不报警。
GROUP_SPIKE_FRAC = 0.5       # 超过半数成员同时陡峰 → 自然现象，不报

# ===== 报警图时间范围（聚焦台阶，只用于报警图）=====
ALERT_BACK_MIN = 20          # 突变点往前回退多少分钟（铺垫，看平时斜率）
ALERT_FWD_MIN = 20           # 突变点往后延伸多少分钟（覆盖回落段）

# ===== 报警去抖：同一(场,成员,指标)在这么多分钟内只报一次 =====
ALERT_DEDUP_MIN = 60

# ===== 监控自动停止 =====
AUTO_STOP_HOURS = 72      # 一轮监控满 72 小时后自动停（你也可在表格手动勾停止）

